

var a=5;
var b=7;
var c=2;
var answer=0;



//print the initial value of answer
document.write(answer);
document.write("<br>");


//variable mutation (changing the value of a variable)
answer=a+b;
document.write(answer); //initial value of answer set to 0
document.write("<br>");


//order of operations
answer=a+b*c;
document.write(answer);
document.write("<br>");

answer=(a+b)*c     //using bracket to change order of operations
document.write(answer);
document.write("<br>");
